// Increment and  Decrement OPERATOR :--

let n = 5;

console.log(n + " " +  ++n + " " +  n++);   // ++n  is a pre increment and it will change value first then print
console.log(n);                             // n++  is a post increment and it will change value in the second print not in the first time 

console.log(n + " " +  --n + " " +  n--);   // ++n  is a pre increment and it will change value first then print
console.log(n);                             // n++  is a post increment and it will change value in the second print not in the first time 